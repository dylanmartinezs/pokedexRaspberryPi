    public void setPokes(int i)
    {
        try
        {
            if(index <= 5)
                goUpList.setVisible(false);
            else
                goUpList.setVisible(true);
                
            rs.absolute(index);
            firstPokeImg.setIcon(new javax.swing.ImageIcon(getClass().getResource("/mainPackage/Images/pokes/"+rs.getString("POKENAME").toLowerCase()+".png")));
            firstPoke.setText(index+" - "+rs.getString("POKENAME"));

            rs.absolute(index + 1);
            secondPokeImg.setIcon(new javax.swing.ImageIcon(getClass().getResource("/mainPackage/Images/pokes/"+rs.getString("POKENAME").toLowerCase()+".png")));
            secondPoke.setText((index + 1)+" - "+rs.getString("POKENAME"));

            rs.absolute(index + 2);
            thirdPokeImg.setIcon(new javax.swing.ImageIcon(getClass().getResource("/mainPackage/Images/pokes/"+rs.getString("POKENAME").toLowerCase()+".png")));
            thirdPoke.setText((index + 2)+" - "+rs.getString("POKENAME"));

            rs.absolute(index + 3);
            fourthPokeImg.setIcon(new javax.swing.ImageIcon(getClass().getResource("/mainPackage/Images/pokes/"+rs.getString("POKENAME").toLowerCase()+".png")));
            fourthPoke.setText((index + 3)+" - "+rs.getString("POKENAME"));

            rs.absolute(index + 4);
            fifthPokeImg.setIcon(new javax.swing.ImageIcon(getClass().getResource("/mainPackage/Images/pokes/"+rs.getString("POKENAME").toLowerCase()+".png")));
            fifthPoke.setText((index + 4)+" - "+rs.getString("POKENAME"));
        }
        catch(SQLException e) {System.out.println(e.getMessage());}
        
    }
    public void setPokeDesc()
    {
        try
        {
            String sql = "select * from main_puchamones where dexno = "+current;
            rs2 = state2.executeQuery(sql);
            rs2.first();
            PokeInfoName.setText(rs2.getString("POKENAME"));
            PokeTypeLabel1.setText(rs2.getString("Type1"));
            switch(rs2.getString("Type1"))
            {
                case "Grass":
                    PokeType1.setBackground(new java.awt.Color(7, 127, 63));
            }
            if(rs2.getString("Type2") != null)
            {
                PokeType2.setVisible(true);
                PokeTypeLabel2.setText(rs2.getString("Type2"));
            }
            else
            {
                PokeType2.setVisible(false);
            }
            if(Integer.parseInt(rs2.getString("DEXNO")) == 1)
            {
                prevPokeInfo.setVisible(false);
            }
            else
                prevPokeInfo.setVisible(true);
            
            if(Integer.parseInt(rs2.getString("DEXNO")) < 10)
                pokeNo.setText("#00"+rs2.getString("DEXNO"));
            else
                if(Integer.parseInt(rs2.getString("DEXNO")) < 100)
                    pokeNo.setText("#0"+rs2.getString("DEXNO"));
            else
                pokeNo.setText(rs2.getString("DEXNO"));
        }
        catch(SQLException e) {System.err.println(e.getMessage());}
    }

